    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblListMyStudents.getSelectedRow();
        int GPA = Integer.parseInt(txtGPA.getText());
        StudentProfile sp = facultyProfile.getStudentList().get(selectedRow);

        StudentDirectory studentDir = business.getStudentdirectory();
        StudentProfile studentFoundRef = studentDir.findStudent(sp.getPerson().getId());

        CourseDirectory courseDir = business.getCourseDirectory();
        System.out.println("This!:" + String.valueOf(tblListMyStudents.getModel().getValueAt(selectedRow, 2)));
        Course foundCourseRef = courseDir.searchCourse(String.valueOf(tblListMyStudents.getModel().getValueAt(selectedRow, 2)));

        studentFoundRef.assignGradeByCourse(foundCourseRef, GPA);

        populateTable();
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);

        JOptionPane.showMessageDialog(null, "Course updated successfully!");
    }//GEN-LAST:event_btnSaveActionPerformed

