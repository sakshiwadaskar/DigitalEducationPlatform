    private void populateTable() {
        Set<Course> foundCourseSet = new HashSet<>();
        DefaultTableModel dtm = (DefaultTableModel) tblListMyStudents.getModel();
        dtm.setRowCount(0);
        String courseName = "";
        String courseId = "";
        Course course = null;
        Integer gpa = 0;
        for (StudentProfile sp : facultyProfile.getStudentList()) {
            System.out.println(sp.getPerson());

            for (Course c : sp.getRegisteredCourseList()) {
                if (c.getFacultyProfile().getPerson().getName().equals(facultyProfile.getPerson().getName())
                        && !foundCourseSet.contains(c)) {
                    courseName = c.getCourseName();
                    courseId = c.getCourseID();
                    course = c;
                    foundCourseSet.add(c);
                    break;
                }
            }

            for (Map.Entry<Course, Integer> entry : sp.getGpaMap().entrySet()) {
                System.out.println(entry.getKey() + " : " + entry.getValue());
                if (entry.getKey().getCourseName().equals(courseName)) {
                    gpa = entry.getValue();
                }
            }

            Object[] row = new Object[7];
            row[0] = sp.getPerson().getId();
            row[1] = sp.getPerson().getName();
            row[2] = courseId;
            row[3] = courseName;
            row[4] = gpa;
            dtm.addRow(row);
        }

    }

